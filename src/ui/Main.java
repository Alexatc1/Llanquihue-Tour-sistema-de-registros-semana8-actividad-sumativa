    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String tipoUsuario = cmbEntidad.getSelectedItem().toString();
        String nombre = txtNombre.getText().trim();
        String rut = txtRut.getText().trim();
        String datoEspecifico = txtDato.getText().trim();
        

        if (nombre.equals("") || rut.equals("") || datoEspecifico.equals("")) {

            JOptionPane.showMessageDialog(
                    this,
                    "Porfavor complete todos los campos antes de registrar."
            );

            return;
        }

        if (tipoUsuario.equals("Guia")) {

            GuiaTuristico guiaturistico = new GuiaTuristico(
                    datoEspecifico,
                    nombre,
                    rut
            );

            gestorentidades.agregarEntidad(guiaturistico);

            JOptionPane.showMessageDialog(
                    this,
                    "Guia turistico agregado exitosamente."
            );

        } else if (tipoUsuario.equals("Colaborador")) {

            ColaboradorExterno colaboradorexterno = new ColaboradorExterno(
                    datoEspecifico,
                    nombre,
                    rut
            );

            gestorentidades.agregarEntidad(colaboradorexterno);

            JOptionPane.showMessageDialog(
                    this,
                    "Colaborador Externo agregado exitosamente."
            );
        }
        
        limpiarCampos();
    }//GEN-LAST:event_btnRegistrarActionPerformed

