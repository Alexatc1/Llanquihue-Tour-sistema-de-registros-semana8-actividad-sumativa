    private void btnRegistrarVehiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVehiculoActionPerformed
        String patente = txtPatente.getText().trim();
        String marca = txtMarca.getText().trim();
        
        if (patente.equals("") || marca.equals("")) {

            JOptionPane.showMessageDialog(
                    this,
                    "Porfavor complete todos los campos antes de registrar."
            );

            return;
        }
        
        Vehiculo vehiculo = new Vehiculo (
        patente,
        marca);
        
        gestorentidades.agregarEntidad(vehiculo);
        JOptionPane.showMessageDialog(
                    this,
                    "vehiculo agregado de manera exitosa."
            );
        
        limpiarCampos();
    }//GEN-LAST:event_btnRegistrarVehiculoActionPerformed

